# Datatypes : 
'''
 --The different types of data we stores in variables is called datetypes
 --for eg.  int , float, double, complex, str, boolean, None.
 -- List , tuple, dict and more.
'''

a = 'sanket'                          # str
b = 21                                # int
c = 21.00                             #float
d = 12+0j or complex(12,0)            # Complex
e = True or False                     # bool
f = None                              # None


print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f)) 

'''===================================================================================================='''

# list :
'''
 --list is another datatype in python.
 --it is collection of different types of data.
 --it is mutable in nature
 --for example
'''
list1 = [1,2, [1,2], ['sanket', '..a.u'], True, False]
print(type(list1))

'''====================================================================================================='''

# tuple :
'''
 --tuple is also type of datatype
 --it is similar to the list we can store differnt types of value in it.
 --but tuple is immutable in nature so we can not change the values in it.
 --for example
'''
tuple1 = ( 1, 2, [1,2], 'sanket', '..a.u', 0.11)
print(type(tuple1))

'''======================================================================================================'''

# dict :
'''
 --dictionary is type of datatype
 --it can store value with pair form or mapped data
 --for example
'''
dict1 = {'king':"Queen", 'sanket':'..a.j', 1:'adf', 12 : 123}
print(type(dict1))

