# break in python :
'''
--it can use for breaking the loop. 
--'''

for i in range( 10):
    if( i ==5 ):
        break
    else:
        print(i)

print("--------------------------------\n\n")
# continue 
'''
--continue statement is skip the perticular interation'''

for i in range( 10 ):
    if ( i ==5 ):
        continue
    else:
        print(i)