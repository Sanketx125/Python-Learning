# Arithmetic Operator's :
'''
        |===================|==================================|=======================|=========================|
        |                   |                                  |                       |                         |
        |    operator's     |              name                |       example-1       |      example-2          |
        |                   |                                  |                       |                         |
        |===================|==================================|=======================|=========================|
        |                   |                                  |                       |                         |
        |        +          |           addition               |         a+b           |        5+2 = 7          |
        |                   |                                  |                       |                         |
        |        -          |           subtraction            |         a-b           |        5-2 = 3          |
        |                   |                                  |                       |                         |
        |        *          |           Multiplication         |         a*b           |        5*2 = 10         |
        |                   |                                  |                       |                         |
        |        **         |           Power                  |         a**b          |        5**2 = 25        |
        |                   |                                  |                       |                         |
        |        /          |           division               |         a/b           |        5/2 = 2.5        |
        |                   |                                  |                       |                         |
        |        //         |          floor division          |         a//b          |        5//2 = 2         |
        |                   |                                  |                       |                         |
        |        %          |           modular                |         a%b           |        5%2 = 1          |
        |                   |                                  |                       |                         |
        |===================|==================================|=======================|=========================|
'''

a = 5
b = 2

#addition :- add two numbers
print(a+b)  

#subtraction:- subtract two number
print(a-b)

#multiplication :- multiply two number
print(a*b)

#Power :- show power of value , where a-value and b-power        eg. ( a**n  =  a*a*a*......a^n) or a^n 
print(a**b)

#division :- divide two number
print(a/b)

#floor division:- it divide two number but show only interger value not float
print(a//b)

#modular :- it divide value but show only reminder value
print(a%b)






